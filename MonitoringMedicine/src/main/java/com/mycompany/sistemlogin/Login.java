    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Statement statement = null;
        try {
            statement = con.createStatement();
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }

        String pwd = new String(jPasswordField1.getPassword());
        String username = new String(jTextField2.getText());
        
        if (pwd.equalsIgnoreCase("") ||
                username.equalsIgnoreCase("")) {
                    JOptionPane.showMessageDialog(null, "Harap isi semua field terlebih dahulu");
            } else {

            try {
                SistemLogin data =  new SistemLogin();
                Statement query = con.createStatement();
                ResultSet rs = query.executeQuery("select * from list_user where username like '"+username+"' and password like '"+pwd+"'");
//                jButton1.setEnabled(false);
//                System.out.println(rs);
                String id = "";
                while (rs.next()) {
                    data.setUserId(rs.getString(1));
                    id = rs.getString(1);
                }
                
                if (id.equalsIgnoreCase("")){
                    JOptionPane.showMessageDialog(null, "Username/Password Salah");
                    setVisible(true);
                } else{
                    setVisible(false);
                    new Home( Integer.parseInt(id)); 
                }
            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

